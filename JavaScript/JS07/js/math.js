// 向下取整
console.log(Math.floor(3.75));

// 向上取整
console.log(Math.ceil(2.21));


// 四舍五入
console.log(Math.round(5.5));

// 幂运算
console.log(Math.pow(5, 3));


// 开方
console.log(Math.sqrt(2).toFixed(6));

// 求最小数
console.log(Math.min(5, 3, 4, 56));

// 求最大数
console.log(Math.max(4, 3, 90, 12));

// 求正弦值
console.log(Math.sin(Math.PI / 4));

// 求余弦值
console.log(Math.cos(Math.PI / 3));

// 正切
console.log(Math.tan(Math.PI / 4));

